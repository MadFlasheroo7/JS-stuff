/**
 * for loop consists of three optional expression to be executed in the loop.
 * 
 * learn more - https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Statements/for
 */

 var states = [
    "Delhi",
    "Punjab",
    "Tamil Nadu",
    "Assam",
];

for (let i = 0; i < states.length; i++) {
    console.log(states[i]);
}