/**
 * for each is more like array method it executes
 * provided function for each element in array.
 * 
 * learn more - https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Array/forEach
 */

 var states = [
    "Delhi",
    "Punjab",
    "Tamil Nadu",
    "Assam",
];

states.forEach(element => {console.log(element);});
// output - 
// Delhi
// Punjab
// Tamil Nadu
// Assam