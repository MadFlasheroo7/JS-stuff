/**
 * If else executes if the specified condition is truthy(true) and 
 * if the condition is falsy(false) execute another statement.
 * 
 * learn more - https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Statements/if...else
 */

let value = 5;

if (value < 10) {
    console.log("value is less than 5");
} else {
    console.log("value is greater than 10");
} // value is less than 5